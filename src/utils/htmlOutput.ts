const HTML_OUTPUT_DIR = join(tmpdir(), 'claude-html')

/**
 * Sanitize a filename for cross-platform safety.
 * Removes path separators and restricts to safe characters.
 */
function sanitizeFilename(name: string): string {
  return name
    .replace(/[/\\:*?"<>|]/g, '_')
    .replace(/\s+/g, '_')
    .slice(0, 200)
}

