/**
 * Wrap user-provided HTML content in a complete, self-contained document with
 * responsive base styles. If the content already contains <html> or <body> tags,
 * returns it as-is (assumes the caller provided a complete document).
 */
function wrapHtmlDocument(innerHtml: string, title: string): string {
  // If already a full document, return as-is
  if (/<html/i.test(innerHtml) || /<body/i.test(innerHtml)) {
    return innerHtml
  }

  // Escape accidental closing tags in content
  const safeContent = innerHtml
    .replace(/<\/html>/gi, '')
    .replace(/<\/body>/gi, '')

  return `<!DOCTYPE html>
<html lang="zh-CN">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${escapeHtml(title)}</title>
  <style>${DEFAULT_CSS}</style>
</head>
<body>
${safeContent}
  <footer>
    <p>Generated by MiniClaude · ${new Date().toLocaleString('zh-CN', { timeZone: 'Asia/Shanghai' })}</p>
  </footer>
</body>
</html>`
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
}

/**
 * Write self-contained HTML content to a temp file and optionally open it
 * in the system browser.
 *
 * @param content  - The HTML content (fragment or full document)
 * @param options  - { filename?: string; title?: string; open?: boolean }
 * @returns The absolute path to the written HTML file
 *
 * @example
 *   const path = await writeHtmlOutput('<h1>Hello</h1>', { title: 'Report' })
 *   // → 'C:\\Users\\...\\AppData\\Local\\Temp\\claude-html\\Report.html'
 *   // File is auto-opened in browser
 */
export async function writeHtmlOutput(
  content: string,
  options: {
    filename?: string
    title?: string
    open?: boolean
  } = {},
): Promise<string> {
  const filename = sanitizeFilename(
    options.filename || `mini-claude-output-${Date.now()}`,
  )
  const title = options.title || 'MiniClaude Output'
  const shouldOpen = options.open !== false // default: true (auto-open)

  // Ensure filename ends with .html
  const htmlFilename = filename.endsWith('.html') ? filename : `${filename}.html`

  // Create output directory
  if (!existsSync(HTML_OUTPUT_DIR)) {
    mkdirSync(HTML_OUTPUT_DIR, { recursive: true })
  }

  const filePath = join(HTML_OUTPUT_DIR, htmlFilename)
  const fullDocument = wrapHtmlDocument(content, title)

  try {
    writeFileSync(filePath, fullDocument)
    logForDebugging(`HTML output written to ${filePath} (${fullDocument.length} bytes)`)

    if (shouldOpen) {
      const opened = await openPath(filePath)
      if (!opened) {
        logForDebugging(`Failed to auto-open browser for ${filePath}`, { level: 'info' })
      }
    }
  } catch (error) {
    logError(error)
    throw new Error(
      `Failed to write HTML output: ${error instanceof Error ? error.message : String(error)}`,
    )
  }

  return filePath
}

/**
 * Quick one-liner: write HTML and return the path.
 * Opens in browser by default. Use when you don't need to configure options.
 */
export async function html(content: string, title?: string): Promise<string> {
  return writeHtmlOutput(content, { title })
}
